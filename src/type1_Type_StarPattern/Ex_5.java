package type1_Type_StarPattern;

public class Ex_5 {

	static int r,c,n=5;
	public static void main(String[] args) {
		
		for (int r=1; r<=n; r++)
		{
			
			for (int c=1; c<=n; c++)
			{
				System.out.print( c+ " " );
			}
			
			System.out.println();
			
		}
			
	}
}

